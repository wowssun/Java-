package pr230501;

public class EachPerson {

	private String name;
	private String phonenum;
	private String birth;
	
	public EachPerson() {
		super();
	}

	public EachPerson(String name, String phonenum, String birth) {
		super();
		this.name = name;
		this.phonenum = phonenum;
		this.birth = birth;
	}

	public EachPerson(String name, String phonenum) {
		super();
		this.name = name;
		this.phonenum = phonenum;
	}
    
	
	
	
}
